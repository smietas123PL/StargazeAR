export function resolveInitialLanguage(): Language {
  if (typeof window === "undefined") return "en";

  try {
    const stored = window.localStorage.getItem(LANGUAGE_STORAGE_KEY);
    if (stored === "en" || stored === "pl") return stored;
  } catch {
    // Ignore storage access issues and fall back to navigator settings.
  }

  if (typeof navigator !== "undefined") {
    const preferred = navigator.language.toLowerCase();
    if (preferred.startsWith("pl")) return "pl";
  }

  return "en";
}

export function setCurrentLanguage(nextLanguage: Language) {
  currentLanguage = nextLanguage;
}

export function getCurrentLanguage(): Language {
  return currentLanguage;
}

export function getLocaleForLanguage(language: Language = currentLanguage): string {
  return language === "pl" ? "pl-PL" : "en-US";
}

function translateCoreText(text: string, language: Language): string {
  if (language === "en") return text;

  const exact = EXACT_TRANSLATIONS_PL[text];
  if (exact) return exact;

  for (const translator of REGEX_TRANSLATIONS_PL) {
    const match = translator.pattern.exec(text);
    if (match) return translator.format(match);
  }

  return text;
}

export function translateText(text: string, language: Language = currentLanguage): string {
  if (!text) return text;

  const match = /^(\s*)(.*?)(\s*)$/s.exec(text);
  if (!match) return translateCoreText(text, language);

  const [, leading, core, trailing] = match;
  return `${leading}${translateCoreText(core, language)}${trailing}`;
}

export function translateMaybeText(
  text: string | null | undefined,
  language: Language = currentLanguage,
): string | null | undefined {
  if (text == null) return text;
  return translateText(text, language);
}

export function translateReactNode(node: ReactNode, language: Language = currentLanguage): ReactNode {
  if (typeof node === "string") return translateText(node, language);
  return node;
}

